        /// <summary>
        /// 判断是否为合法的手机号码
        /// </summary>
        public static bool IsRightfulMobile(string mobiles)
        {
            string[] mos = mobiles.Split(",".ToCharArray());
            string pattern = @"^1[3|4|5|8]\d{9}$";
            foreach (string mo in mos)
            {
                if (!Regex.IsMatch(mo, pattern))
                    return false;
            }
            return true;
        }
